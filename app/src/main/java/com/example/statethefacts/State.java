package com.example.statethefacts;

public class State {
    String abbreviation;
    String capital;
    String state;
    String flower;
    String rock;
    String governor;
    String bird;

    public State(String abbreviation, String capital, String state, String flower,
                 String rock, String governor, String bird) {
        this.abbreviation = abbreviation;
        this.capital = capital;
        this.state = state;
        this.flower = flower;
        this.rock = rock;
        this.governor = governor;
        this.bird = bird;
    }
}
